@interface TreasureHuntRenderer() {
    // GL variables for the cube.
    GLuint _program;
    GLuint _vertexArray;
    GLuint _vertexBuffer;
    GLKMatrix4 _modelViewBaseMatrix;
    GLKMatrix4 _normalBaseMatrix;
    GLKMatrix3 _normalMatrix;
    GLKMatrix4 _walkingMatrix;
    GLfloat _offset[3];
    BOOL _walking;
}

- (void)setupGL;
- (void)tearDownGL;

- (BOOL)loadShaders;
- (BOOL)compileShader:(GLuint *)shader type:(GLenum)type file:(NSString *)file;
- (BOOL)linkProgram:(GLuint)prog;
