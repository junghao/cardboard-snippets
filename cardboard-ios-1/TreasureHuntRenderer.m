@implementation TreasureHuntRenderer

#pragma mark - GVRCardboardViewDelegate overrides

- (void)cardboardView:(GVRCardboardView *)cardboardView
     willStartDrawing:(GVRHeadTransform *)headTransform {
    
    [self setupGL];
    
    _modelViewBaseMatrix = GLKMatrix4MakeTranslation(0.0f, 0.0f, -2.0f);
    GLKMatrix4 m = GLKMatrix4Multiply(GLKMatrix4Transpose([headTransform headPoseInStartSpace]),
                                      _modelViewBaseMatrix);
    _normalMatrix = GLKMatrix3InvertAndTranspose(GLKMatrix4GetMatrix3(m), NULL);

    _offset[0] = 0.0f;
    _offset[1] = 0.0f;
    _offset[2] = 0.0f;
    
    _walking = NO;
}

- (void)cardboardView:(GVRCardboardView *)cardboardView
     prepareDrawFrame:(GVRHeadTransform *)headTransform {
    glClearColor(0.65f, 0.65f, 0.65f, 1.0f);
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
    glEnable(GL_DEPTH_TEST);
    glEnable(GL_SCISSOR_TEST);
    
    if(_walking) {
        float d = 0.05f;
        
        const float *head_matrix = GLKMatrix4Transpose([headTransform headPoseInStartSpace]).m;
        float x = d * head_matrix[8];
        float y = d * head_matrix[9];
        float z = d * head_matrix[10];
        
        _offset[0] += x;
        _offset[1] += y;
        _offset[2] += z;
    }
}

- (void)cardboardView:(GVRCardboardView *)cardboardView
              drawEye:(GVREye)eye
    withHeadTransform:(GVRHeadTransform *)headTransform {
    // Get the head matrix.
    const GLKMatrix4 head_from_start_matrix = [headTransform headPoseInStartSpace];

    // Get this eye's matrices.
    GLKMatrix4 projection_matrix = [headTransform projectionMatrixForEye:eye near:0.1f far:100.0f];
    GLKMatrix4 eye_from_head_matrix = [headTransform eyeFromHeadMatrix:eye];

    // Compute the model view projection matrix.
    GLKMatrix4 model_view_projection_matrix =
        GLKMatrix4Multiply(projection_matrix,
                           GLKMatrix4Multiply(eye_from_head_matrix, head_from_start_matrix));

    model_view_projection_matrix = GLKMatrix4Translate(model_view_projection_matrix,
                                                       _offset[0], _offset[1], _offset[2]);
    model_view_projection_matrix = GLKMatrix4Multiply(model_view_projection_matrix,
                                                      _modelViewBaseMatrix);
    glBindVertexArrayOES(_vertexArray);
    glUseProgram(_program);
    
    for(int i=0; i<10; i++) {
        GLKMatrix4 matrix = GLKMatrix4Translate(model_view_projection_matrix, 1.1f * i, 0.0f, 0.0f);
        glUniformMatrix4fv(uniforms[UNIFORM_MODELVIEWPROJECTION_MATRIX], 1, 0, matrix.m);
        glUniformMatrix3fv(uniforms[UNIFORM_NORMAL_MATRIX], 1, 0, _normalMatrix.m);
        glDrawArrays(GL_TRIANGLES, 0, 36);
    }
}

- (void)cardboardView:(GVRCardboardView *)cardboardView
         didFireEvent:(GVRUserEvent)event {
  switch (event) {
    case kGVRUserEventBackButton:
      NSLog(@"User pressed back button");
      break;
    case kGVRUserEventTilt:
      NSLog(@"User performed tilt action");
      break;
    case kGVRUserEventTrigger:
      _walking = !_walking;
      break;
  }
}

- (void)cardboardView:(GVRCardboardView *)cardboardView shouldPauseDrawing:(BOOL)pause {
  if ([self.delegate respondsToSelector:@selector(shouldPauseRenderLoop:)]) {
    [self.delegate shouldPauseRenderLoop:pause];
  }

}

